#include <stdio.h>
int main()
{
	//int c[10];
	int a[] = { 10, 20, 30, 40, 50 };//배열->여러개 저장장소 연소적으로
	int* p;//포인터 변수

	p = a;//a주소를 p(주소기억)
	printf("a[0]=%d a[1]=%d a[2]=%d \n", a[0], a[1], a[2]);
	printf("p[0]=%d p[1]=%d p[2]=%d \n\n", p[0], p[1], p[2]);

	p[0] = 60;//같은 주소르 공유하면 그 값이 바뀌면 같이 바뀜
	p[1] = 70;//p와a같은 주소 ->값이 같음
	p[2] = 80;

	printf("a[0]=%d a[1]=%d a[2]=%d \n", a[0], a[1], a[2]);
	printf("p[0]=%d p[1]=%d p[2]=%d \n", p[0], p[1], p[2]);
	return 0;
}
